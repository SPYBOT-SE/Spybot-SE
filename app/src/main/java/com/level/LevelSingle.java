    public static int[][] getLevel(int levelID) {
        if(!initialized) {
            initializeLevels();
            initialized = true;
        }

        if(!levels.containsKey(levelID)) {
            return Template;
        }

        return levels.get(levelID);
    }

    public static String getLevelName(int levelID){
        if(!initialized) {
            initializeLevels();
            initialized = true;
        }

        if(!mapNames.containsKey(levelID)) {
            return "Error";
        }

        return mapNames.get(levelID);
    }


